
public class Point {
    private final int xPoint;
    private final int yPoint;
    public Point(int xPoint, int yPoint){
        this.xPoint = xPoint;
        this.yPoint = yPoint;
    }
    
    public int getXPoint()
    {
        return this.xPoint;
    }
    
    public int getYPoint(){
        return this.yPoint;
    }

}
